#Operaciones aritmeticas con funciones

def suma(a,b):
    return a+b

def resta(a,b):
    return a-b

def multiplicacion(a,b):
    return a*b

def division(a,b):
    return a/b

def potencia(a,b):
    return a**b


print(f"Suma: {suma(1,1)}")

print(f"Resta: {resta(3,1)}")

print(f"Multiplicacion: {multiplicacion(1,1)}")

print(f"Division: {division(2,1)}")

print(f"Potencia: {potencia(2,2)}")


