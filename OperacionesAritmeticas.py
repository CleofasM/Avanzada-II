#Programa que hace operaciones aritmeticas basicas.
a=10
b=5

#suma
suma=a+b
print("Suma= ",suma)

#Resta
resta=a-b
print("Resta=",resta)

#Multiplicacion 
Multi=a*b
print("Multiplicacion=",Multi)

#Division
Divi=a/b
print("Division=",Divi)

#Ingreso de valores a variables
x=int(input("x: "))
y=int(input("y: "))

#Potencia
potencia=x**y
print(f"{x}^{y}=",potencia)

#suma
suma=x+y
print(f"{x}+{y}=",suma)

#Operador de Residuo 
residuo=x%y
print(f"{x}%{y}=",residuo)
